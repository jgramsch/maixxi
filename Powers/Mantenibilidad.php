	<h2>Mantenibilidad</h2>
	<br>
	<ul>
		<li><h3>Mantenibilidad <span lang="latex">\,M(t)</span>:</h3>Para una entidad que opera en ciertas condiciones dadas de utilización, es la probabilidad de que una tarea dada de mantenimiento pueda ser ejecutada dentro de un intervalo de tiempo dado (0,t) cuando la mantención se realiza dentro de condiciones dadas y utilzando procedimiento y medios prescritos.</li>
		<li><h3>Se calcula como:<span lang="latex">\quad M(t)=1-e^{-\int\mu dt}</span></h3>En que <span lang="latex">\mu</span>= tasa de reparación<span lang="latex">
		t</span>= el tiempo apropiado para ejecutar el trabajo (estándar).</li>
		<p>Otro parámetro que se usa para evaluar la mantenibilidad es MTTR: tiempo medio para reparar.</p>
		<p>Cuando <span lang="latex">\mu</span>= constante en el tiempo se da que <span lang="latex">\mu=\frac{1}{MTTR}</span></p>
	</ul>
