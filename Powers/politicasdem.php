	<h2>Políticas de MP, MC, MPRD</h2>
	<br>
	<p><b>MP </b>= Políticas de mantenimiento preventivo</p>
	<p><b>MC </b>= Políticas de mantenimiento correctivo</p>
	<p><b>MPRD </b>= Políticas de mantenimiento predictivo</p>
	<br>
	<p>Todas son formas de mantenimiento sobre las cuales el encargado de mantenimiento tiene capacidad de decisión</p>
	<br>
	<p>En el caso de las <b>Fallas </b> la capacidad de desicion del encargado de mantencion suele ser muy limitada.<small>En la mayor parte de los casos se debe limitar a repararlas lo antes posible</small></p>
