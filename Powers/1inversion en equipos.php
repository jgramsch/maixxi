	<h2>Inversión en equipos de reemplazo</h2>
	<h3>Rentabilidad, Tasa de descuento, Politicas de reemplazo, Overhaul, Costo creciente</h3>
	<br>
	<h4>A medida que los equipos envejecen su mantenimiento encarece y llega un momento en que es más economico reemplazarlos por equipos nuevos en algunos casos una opción es la reparación general (Overhaul)</h4>
	<h5>El procedimiento que se usa para este análisis y para determinar el momento más adecuado de reemplazo es el <i>"MODELO DE REEMPLAZO"</i> que se puede aplicar en diversas situaciones: equipos que se deterioran con el tiempo, equipos que fallan.</h5>
	<br>
	<h4>El modelo de reemplazo utiliza la ecuación de costos</h4>
	<div lang="latex">
		K_n = \{A+C_1+(\frac{C_2}{1+r})+(\frac{C_3}{1+r^2})+\ldots+(\frac{C_n}{(1+r)^{n-1}})\}+\{\frac{A+C_1}{(1+r)^n}+(\frac{C_2}{(1+r)^{n
		+1}})+\ldots+(\frac{C_n}{(1+r)^{2n-1}})\}
	</div>
	<h4>En que:</h4>
	<ul>
		<li><span lang="latex">A</span>: es el costo inicial de un ítem nuevo</li>
		<li><span lang="latex">C_1,C_2,C_3\ldots</span>: los costos incurridos en los periodos anuales</li>
		<li><span lang="latex">r</span>: la tasa de descuento (costo del dinero en el periodo)</li>
		<li><span lang="latex">K_n</span>: El valor presente de todos los costos futuros asociados con la politica de reemplazar el ítem después de n perdidos</li>
	</ul>
	<h4><ins>Ecuación de costos</ins></h4>
	<h5>Entonces si <span lang="latex">K_n</span> es menor que <span lang="latex">K_{n+1}</span> signidica que reemplazar el equipo cada n años es preferible que reemplazarlo cada n+1 años</h5>
	<h5>Es decir se deben cumplir las dos desigualdades</h5>
	<div lang="latex">
		K_{n+1}-K_n>0 \land K_{n+1}-K_n>0
	</div>
</div>